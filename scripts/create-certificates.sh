  -ca=ca.pem \
  -ca-key=ca-key.pem \
  -config=ca-config.json \
  -profile=kubernetes \
  service-account-csr.json | cfssljson -bare service-account

for instance in worker-0 worker-1 worker-2; do
  scp /root/ca.pem /root/${instance}-key.pem /root/${instance}.pem ${instance}:~/
done

for instance in controller-0 controller-1 controller-2; do
  scp /root/ca.pem /root/ca-key.pem /root/kubernetes-key.pem /root/kubernetes.pem \
    /root/service-account-key.pem /root/service-account.pem ${instance}:~/
done
